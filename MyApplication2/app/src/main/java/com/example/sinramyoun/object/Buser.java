package com.example.sinramyoun.object;

public class Buser {
    String num;
    String name;



    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }




    public Buser(String num, String name) {
        super();
        this.num = num;
        this.name = name;


    }

}
